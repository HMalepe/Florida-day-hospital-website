#!/usr/bin/env bash
set -euo pipefail

PUBLIC_SITE="$(printf '%s' "${PUBLIC_SITE:-}" | tr '[:upper:]' '[:lower:]' | xargs)"
SITE_VIEW="$(printf '%s' "${SITE_VIEW:-auto}" | tr '[:upper:]' '[:lower:]' | xargs)"
ROOT="$(cd "$(dirname "$0")/.." && pwd)"
OUT="$ROOT/_site"

inject_hero_preload() {
  local dest="$1"
  if command -v node >/dev/null 2>&1; then
    node "$ROOT/scripts/inject-hero-preload.js" "$dest"
  else
    echo "inject-hero-preload: node not found on PATH — skipping static preload injection" >&2
  fi
}

write_site_config() {
  local dest="$1"
  local view="$SITE_VIEW"

  case "$view" in
    mobile|desktop) ;;
    *) view="auto" ;;
  esac

  # Public / presentation builds must not default every desktop visitor into
  # the 390px phone frame. Opt in with ALLOW_MOBILE_DEFAULT=true if needed.
  if [ "$view" = "mobile" ] && [ "${ALLOW_MOBILE_DEFAULT:-}" != "true" ]; then
    echo "SITE_VIEW=mobile → writing defaultView:auto (set ALLOW_MOBILE_DEFAULT=true to keep mobile)" >&2
    view="auto"
  fi

  mkdir -p "$dest/js"
  cat > "$dest/js/site-config.js" <<EOF
// Generated by scripts/prepare-site.sh — do not edit in _site
// showViewToggle: set true (and redeploy) to restore Desktop/Mobile preview controls.
window.FDH_SITE_CONFIG = { defaultView: '$view', showViewToggle: false };
EOF
}

deploy_full() {
  local dest="$1"
  local keep_gate="${2:-false}"

  mkdir -p "$dest"
  # tar is available on Vercel; rsync is not
  local excludes=(
    --exclude='.git'
    --exclude='_site'
    --exclude='robots-private.txt'
    --exclude='.github'
    --exclude='scripts'
    --exclude='vercel.json'
    --exclude='vercel.public.json'
    --exclude='middleware.js'
  )

  if [ "$keep_gate" != "true" ]; then
    excludes+=(--exclude='coming-soon.html')
  fi

  tar -C "$ROOT" "${excludes[@]}" -cf - . | tar -C "$dest" -xf -
}

deploy_gated() {
  local dest="$1"
  local gate="$ROOT/coming-soon.html"

  rm -rf "$dest"
  mkdir -p "$dest/css" "$dest/js" "$dest/data"

  cp "$gate" "$dest/index.html"
  cp "$gate" "$dest/404.html"

  for page in "$ROOT"/*.html; do
    base="$(basename "$page")"
    [ "$base" = "coming-soon.html" ] && continue
    cp "$gate" "$dest/$base"
  done

  for stale in \
    "$dest/js/site.js" \
    "$dest/js/contact.js" \
    "$dest/js/mobile-ui.js" \
    "$dest/js/schema.js" \
    "$dest/js/providers.js" \
    "$dest/js/trust.js" \
    "$dest/js/book-form.js" \
    "$dest/js/photography.js" \
    "$dest/data/contact.json" \
    "$dest/data/providers.json" \
    "$dest/data/trust.json" \
    "$dest/data/photography.json" \
    "$dest/site.webmanifest" \
    "$dest/sitemap.xml"
  do
    cp "$gate" "$stale"
  done

  cp "$ROOT/css/site.css" "$dest/css/"
  cp "$ROOT/favicon.svg" "$dest/" 2>/dev/null || true
  cp "$ROOT/robots-private.txt" "$dest/robots.txt"
}

rm -rf "$OUT"
mkdir -p "$OUT"

if [ "$PUBLIC_SITE" = "true" ]; then
  echo "PUBLIC_SITE=true — deploying full site"
  deploy_full "$OUT"
  inject_hero_preload "$OUT"
elif [ "${GITHUB_PAGES:-}" = "true" ]; then
  echo "PUBLIC_SITE=${PUBLIC_SITE:-<unset>} — GitHub Pages gate deploy"
  deploy_gated "$OUT"
  # No inject_hero_preload here — deploy_gated replaces index.html with
  # the coming-soon page, which has no hero photo mount.
else
  echo "PUBLIC_SITE=${PUBLIC_SITE:-<unset>} — Vercel full site; only floridadayhospital + *.vercel.app allowed via middleware"
  deploy_full "$OUT" true
  cp "$ROOT/robots-private.txt" "$OUT/robots.txt"
  inject_hero_preload "$OUT"
fi

write_site_config "$OUT"
echo "SITE_VIEW=${SITE_VIEW} — default view written to _site/js/site-config.js"
